# Variables are containers for storing data values.
x = 5
y = "John"
print(x)
print(y)

a = 3       # x is of type int
a = "Sally" # x is now of type str
print(a)

# string variable can be declare ".." or '..'

# Casting

S = str(1)  # S will be string '1'
I = int(1)  # I will be int 1
N = float(1)# N will be float 3.0

# when we want to know data type

print(type(S))
print(type(I))
print(type(N))

# Case-Sensitive

J = 1 # int 1
print(type(J))
j = "1" # string 1
print(type(j))

# J and j will not be same
