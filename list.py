# lists are used to store multiple items in a single variable
# There are 4 different types
# others 3 are Tuple, Set, Dictionary

# list
# ordered, changeable, and duplicate values
mylist = ["apple","orange","cherry"]
print(mylist)
# List length
print(len(mylist))

# list items
