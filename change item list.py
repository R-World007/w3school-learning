# change the list item
thislist = ['apple','orange','banana']
thislist[1] = "mango"
print(thislist)

# change the range items

list1 = ['apple','orange','kiwi','banana','cherry','mango','water']
list1[1:3] = ['watermelon','pineapple'] # index 3 is stop point
print(list1)

# change the second value by replacing it with two new values

list2 = ['apple','banana','cherry']
list2[1:2] = ['pineapple','mango']
print(list2)

#Insert Items   insert()
list3 = ["Sai","Moe","Hein",]
list3.insert(2,"Moe")
print(list3)


