#remove() Specified item
list1=['Apple','Banana','Cherry']
list1.remove('Banana')
print(list1)

#pop() Remove Specified Index
list2= ['hi','hola','Hey']
list2.pop(1)
print(list2)
list2= ['hi','hola','Hey']
list2.pop() # remove the last item
print(list2)

# DEL
list3 = ['apple','cherry','banana']
del list3[0]
print(list3)

list4 = ['hi','hey']
del list4 # del the entire list

# clear()
list5 = ['cherry','banana','apple']
list5.clear()
