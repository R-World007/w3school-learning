# data type is an important concept.
# variable can store data of different types, and different types can do different.

   #specific_data_type
 
# text type - str

a = str("Hello world")

# numeric types - int, float, complex

b = int(1)
c = float(1.2)
d = complex(1j)

# sequence types - list, tuple, range

e = list(("apple","orange","banana"))
f = tuple(("apple","orange","banana"))
g = range(6)

# mapping type - dict

h = dict(name="Sai", age = 16)

# set types - set, frozenset

i = set(("apple","orange","banana"))
j = frozenset(("apple","orange","banana"))

# boolean type - bool

k = bool(4)

# binary types - bytes, bytearray, memoryview
l = bytes(5)
m = bytearray(5)
n = memoryview(bytes(5))


# none type - NoneType

o = None

# output
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
print(k)
print(l)
print(m)
print(n)
print(o)



