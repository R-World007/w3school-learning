# data type is an important concept.
# variable can store data of different types, and different types can do different.

# text type - str

a = "Hello world"

# numeric types - int, float, complex

b = 1
c = 1.2
d = 1j

# sequence types - list, tuple, range

e = ["apple","orange","banana"]
f = ("apple","orange","banana")
g = range(6)

# mapping type - dict

h = {"name" :"Sai", "age" : 16}

# set types - set, frozenset

i = {"apple","orange","banana"}
j = frozenset({"apple","orange","banana"})

# boolean type - bool

k = True

# binary types - bytes, bytearray, memoryview
l = b"Hello"
m = bytearray(5)
n = memoryview(bytes(5))


# none type - NoneType

o = None

# output
print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)
print(k)
print(l)
print(m)
print(n)
print(o)



